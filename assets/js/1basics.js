// 1. window object
    /*
    Interact with the browser window
    window.alert("<message>");
    This will provide an alert to the user.
    In modern JS, we avoid the `window` keyword,
    and simply write `alert('<message>`);`
    Example :  alert('hello');
    Window object provides some methods like this 
    to interact with the user through the browser window
    */
    alert("Hello!"); //Alerts the user
    prompt("What do you want?"); // Asks the user for some inputs
    confirm("Are you sure you want to delete?"); // Confirms actions
// 2. Document object - Access to the loaded HTML document DOM
    document.write("Hello");
// 3. Console object - Write into the debugging console of the browser.
    // Debugging purposes - for developers' use
    console.log("Logging something...");
    console.warn("Warning about something...");
